// Import React and Component
import React, {useState, useEffect} from 'react';
import {
  ActivityIndicator,
  View,
  StyleSheet,
  Image,
  ImageBackground
} from 'react-native';

import AsyncStorage from '@react-native-community/async-storage';

const SplashScreen = ({navigation}) => {
  
  //State for ActivityIndicator animation
  const [animating, setAnimating] = useState(true);

  useEffect(() => {
    setTimeout(() => {
      setAnimating(false);
      //Check if user_id is set or not
      //If not then send for Authentication
      //else send to Home Screen
      AsyncStorage.getItem('role').then((value) => {
        if(value === null) navigation.replace('Auth');
        else {
          console.log(value);
          if(value === '"0"') navigation.replace('DrawerNavigationRoutesAdmin');
          else if(value === '"1"') navigation.replace('DrawerNavigationRoutes');
        }
      }
        // navigation.replace(
        //   value === null ? 'Auth' : 'DrawerNavigationRoutes'
        // ),
      );
    }, 5000);
  }, []);

  return (
    <View>
      <ImageBackground
        source={require('../assets/anh2.png')}
        style={{resizeMode: 'cover', flex: 1, justifyContent: "center", alignItems: "center"}}
      />
      <ActivityIndicator
        animating={animating}
        color="#FFFFFF"
        size="large"
        style={styles.activityIndicator}
      />
    </View>
  );
};

export default SplashScreen;

const styles = StyleSheet.create({
  container: {
    flex: 1,
    alignItems: 'center',
    justifyContent: 'center',
  },
  activityIndicator: {
    alignItems: 'center',
    height: 80,
  },
});

